#define LOWER_TO_UPPER_COUNT (int)(sizeof(lower_to_upper)/sizeof(lower_to_upper[0]))

int utf8_tolower(int cp) {
    return case_lookup(upper_to_lower, UPPER_TO_LOWER_COUNT, cp);
}

int utf8_toupper(int cp) {
    return case_lookup(lower_to_upper, LOWER_TO_UPPER_COUNT, cp);
}

int utf8_casefold(int cp) {
    /* Case folding is mostly lowercase, with a few special cases */
    int lo = utf8_tolower(cp);
    /* micro sign -> mu */
    if (cp == 0x00B5) return 0x03BC;
    /* sharp s stays as-is for simple case fold (full fold would expand to "ss") */
    return lo;
}

/* ---- Unicode category tables ---- */

typedef struct { int lo; int hi; } CodeRange;

static int in_range_table(const CodeRange *table, int n, int cp) {
    int lo = 0, hi = n - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        if (cp < table[mid].lo) hi = mid - 1;
        else if (cp > table[mid].hi) lo = mid + 1;
        else return 1;
    }
    return 0;
}

