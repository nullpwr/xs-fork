int utf8_is_ascii(const char *s, int byte_len) {
    for (int i = 0; i < byte_len; i++) {
        if ((unsigned char)s[i] >= 0x80) return 0;
    }
    return 1;
}

/* ---- String conversion functions ---- */

char *utf8_str_upper(const char *s, int byte_len, int *out_len) {
    char *buf = xs_malloc(byte_len * 4 + 1);
    int wi = 0;
    const char *p = s;
    const char *end = s + byte_len;
    while (p < end) {
        int cp;
        int n = utf8_decode(p, (int)(end - p), &cp);
        if (n < 0) {
            buf[wi++] = *p;
            p++;
        } else {
            int upper = utf8_toupper(cp);
            wi += utf8_encode(upper, buf + wi);
            p += n;
        }
    }
    buf[wi] = '\0';
    if (out_len) *out_len = wi;
    return buf;
}

