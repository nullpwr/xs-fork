/* ---- Grapheme cluster segmentation ----
 *
 * Simplified UAX #29: a grapheme cluster is a base character followed by
 * any combining marks, variation selectors, ZWJ sequences, or regional
 * indicator pairs.
 */

#define ZWJ         0x200D
#define REGIONAL_A  0x1F1E6
#define REGIONAL_Z  0x1F1FF
#define VS16        0xFE0F

static int is_regional_indicator(int cp) {
    return cp >= REGIONAL_A && cp <= REGIONAL_Z;
}

/* Check if cp is an emoji modifier (skin tone) */
static int is_emoji_modifier(int cp) {
    return cp >= 0x1F3FB && cp <= 0x1F3FF;
}

/* Check if cp starts an extended pictographic / emoji base.
 * This is a simplified check covering the most common emoji ranges. */
static int is_emoji_base(int cp) {
    if (cp >= 0x1F600 && cp <= 0x1F64F) return 1; /* emoticons */
    if (cp >= 0x1F300 && cp <= 0x1F5FF) return 1; /* misc symbols & pictographs */
    if (cp >= 0x1F680 && cp <= 0x1F6FF) return 1; /* transport & map */
    if (cp >= 0x1F900 && cp <= 0x1F9FF) return 1; /* supplemental symbols */
    if (cp >= 0x1FA00 && cp <= 0x1FA6F) return 1; /* chess symbols */
    if (cp >= 0x1FA70 && cp <= 0x1FAFF) return 1; /* symbols extended-A */
    if (cp >= 0x2600 && cp <= 0x27BF) return 1;   /* misc symbols, dingbats */
    if (cp >= 0x2300 && cp <= 0x23FF) return 1;   /* misc technical */
    if (cp == 0x200D) return 0;                    /* ZWJ is not a base */
    return 0;
}

/* Check for hangul jamo types for syllable composition */
#define HANGUL_L_BASE  0x1100
#define HANGUL_V_BASE  0x1161
#define HANGUL_T_BASE  0x11A7
#define HANGUL_L_END   0x1112
#define HANGUL_V_END   0x1175
#define HANGUL_T_END   0x11C2
#define HANGUL_S_BASE  0xAC00
#define HANGUL_S_END   0xD7A3

static int is_hangul_l(int cp) { return cp >= HANGUL_L_BASE && cp <= HANGUL_L_END; }
static int is_hangul_v(int cp) { return cp >= HANGUL_V_BASE && cp <= HANGUL_V_END; }
static int is_hangul_t(int cp) { return cp >= (HANGUL_T_BASE+1) && cp <= HANGUL_T_END; }
static int is_hangul_lv(int cp) {
    if (cp < HANGUL_S_BASE || cp > HANGUL_S_END) return 0;
    return ((cp - HANGUL_S_BASE) % 28) == 0;
}
static int is_hangul_lvt(int cp) {
    if (cp < HANGUL_S_BASE || cp > HANGUL_S_END) return 0;
    return ((cp - HANGUL_S_BASE) % 28) != 0;
}

void grapheme_iter_init(GraphemeIter *it, const char *s, int byte_len) {
    it->data = s;
    it->byte_len = byte_len;
    it->byte_pos = 0;
}

int grapheme_iter_next(GraphemeIter *it, const char **start, int *len) {
    if (it->byte_pos >= it->byte_len) return 0;

    const char *s = it->data + it->byte_pos;
    const char *end = it->data + it->byte_len;
    int cluster_start = it->byte_pos;

    /* Decode the first codepoint */
    int cp;
    int n = utf8_decode(s, (int)(end - s), &cp);
    if (n < 0) {
        /* bad byte, treat as single grapheme */
        it->byte_pos++;
        if (start) *start = s;
        if (len) *len = 1;
        return 1;
    }
    s += n;
    it->byte_pos += n;

    /* Handle CR+LF as single grapheme */
    if (cp == 0x000D && s < end) {
        int cp2;
        int n2 = utf8_decode(s, (int)(end - s), &cp2);
        if (n2 > 0 && cp2 == 0x000A) {
            s += n2;
            it->byte_pos += n2;
            if (start) *start = it->data + cluster_start;
            if (len) *len = it->byte_pos - cluster_start;
            return 1;
        }
    }

    /* Regional indicator pairs */
    if (is_regional_indicator(cp) && s < end) {
        int cp2;
        int n2 = utf8_decode(s, (int)(end - s), &cp2);
        if (n2 > 0 && is_regional_indicator(cp2)) {
            s += n2;
            it->byte_pos += n2;
        }
        if (start) *start = it->data + cluster_start;
        if (len) *len = it->byte_pos - cluster_start;
        return 1;
    }

    /* Hangul jamo syllable composition */
    if (is_hangul_l(cp)) {
        /* L can be followed by L, V, LV, LVT */
        while (s < end) {
            int cp2;
            int n2 = utf8_decode(s, (int)(end - s), &cp2);
            if (n2 < 0) break;
            if (is_hangul_l(cp2)) { s += n2; it->byte_pos += n2; }
            else if (is_hangul_v(cp2) || is_hangul_lv(cp2)) {
                s += n2; it->byte_pos += n2;
                /* consume V and T */
                while (s < end) {
                    n2 = utf8_decode(s, (int)(end - s), &cp2);
                    if (n2 < 0) break;
                    if (is_hangul_v(cp2)) { s += n2; it->byte_pos += n2; }
                    else if (is_hangul_t(cp2)) { s += n2; it->byte_pos += n2; }
                    else break;
                }
                break;
            }
            else if (is_hangul_lvt(cp2)) { s += n2; it->byte_pos += n2; break; }
            else break;
        }
    } else if (is_hangul_lv(cp) || is_hangul_v(cp)) {
        while (s < end) {
            int cp2;
            int n2 = utf8_decode(s, (int)(end - s), &cp2);
            if (n2 < 0) break;
            if (is_hangul_v(cp2)) { s += n2; it->byte_pos += n2; }
            else if (is_hangul_t(cp2)) { s += n2; it->byte_pos += n2; }
            else break;
        }
    } else if (is_hangul_lvt(cp) || is_hangul_t(cp)) {
        while (s < end) {
            int cp2;
            int n2 = utf8_decode(s, (int)(end - s), &cp2);
            if (n2 < 0) break;
            if (is_hangul_t(cp2)) { s += n2; it->byte_pos += n2; }
            else break;
        }
    }

    /* Consume combining marks, variation selectors, and ZWJ sequences */
    while (s < end) {
        int cp2;
        int n2 = utf8_decode(s, (int)(end - s), &cp2);
        if (n2 < 0) break;

        if (utf8_is_combining(cp2)) {
            s += n2;
            it->byte_pos += n2;
            continue;
        }

        /* Variation selectors (FE00-FE0F, E0100-E01EF) */
        if ((cp2 >= 0xFE00 && cp2 <= 0xFE0F) || (cp2 >= 0xE0100 && cp2 <= 0xE01EF)) {
            s += n2;
            it->byte_pos += n2;
            continue;
        }

        /* Emoji modifier (skin tone) after emoji base */
        if (is_emoji_modifier(cp2) && is_emoji_base(cp)) {
            s += n2;
            it->byte_pos += n2;
            continue;
        }

        /* ZWJ sequence: ZWJ followed by another character joins them */
        if (cp2 == ZWJ && (s + n2) < end) {
            const char *peek = s + n2;
            int cp3;
            int n3 = utf8_decode(peek, (int)(end - peek), &cp3);
            if (n3 > 0) {
                s += n2 + n3;
                it->byte_pos += n2 + n3;
                cp = cp3; /* update current for further combining */
                continue;
            }
        }

        break;
    }

    if (start) *start = it->data + cluster_start;
    if (len) *len = it->byte_pos - cluster_start;
    return 1;
}

int utf8_grapheme_count(const char *s, int byte_len) {
    GraphemeIter it;
    grapheme_iter_init(&it, s, byte_len);
    int count = 0;
    while (grapheme_iter_next(&it, NULL, NULL)) count++;
    return count;
}

