/*
 * UTF-8 encoding:
 *   1 byte:  0xxxxxxx                  (U+0000 - U+007F)
 *   2 bytes: 110xxxxx 10xxxxxx         (U+0080 - U+07FF)
 *   3 bytes: 1110xxxx 10xxxxxx 10xxxxxx (U+0800 - U+FFFF)
 *   4 bytes: 11110xxx 10xxxxxx 10xxxxxx 10xxxxxx (U+10000 - U+10FFFF)
 */

#define IS_CONT(c) (((unsigned char)(c) & 0xC0) == 0x80)

int utf8_decode(const char *s, int len, int *codepoint) {
    if (len <= 0) return -1;
    unsigned char c = (unsigned char)s[0];

    if (c < 0x80) {
        *codepoint = c;
        return 1;
    }
    if ((c & 0xE0) == 0xC0) {
        if (len < 2 || !IS_CONT(s[1])) return -1;
        *codepoint = ((c & 0x1F) << 6) | (s[1] & 0x3F);
        if (*codepoint < 0x80) return -1; /* overlong */
        return 2;
    }
    if ((c & 0xF0) == 0xE0) {
        if (len < 3 || !IS_CONT(s[1]) || !IS_CONT(s[2])) return -1;
        *codepoint = ((c & 0x0F) << 12) | ((s[1] & 0x3F) << 6) | (s[2] & 0x3F);
        if (*codepoint < 0x800) return -1; /* overlong */
        if (*codepoint >= 0xD800 && *codepoint <= 0xDFFF) return -1; /* surrogate */
        return 3;
    }
    if ((c & 0xF8) == 0xF0) {
        if (len < 4 || !IS_CONT(s[1]) || !IS_CONT(s[2]) || !IS_CONT(s[3])) return -1;
        *codepoint = ((c & 0x07) << 18) | ((s[1] & 0x3F) << 12) |
                     ((s[2] & 0x3F) << 6) | (s[3] & 0x3F);
        if (*codepoint < 0x10000 || *codepoint > 0x10FFFF) return -1;
        return 4;
    }
    return -1;
}

int utf8_encode(int codepoint, char *buf) {
    if (codepoint < 0) return 0;
    if (codepoint < 0x80) {
        buf[0] = (char)codepoint;
        return 1;
    }
    if (codepoint < 0x800) {
        buf[0] = (char)(0xC0 | (codepoint >> 6));
        buf[1] = (char)(0x80 | (codepoint & 0x3F));
        return 2;
    }
    if (codepoint < 0x10000) {
        buf[0] = (char)(0xE0 | (codepoint >> 12));
        buf[1] = (char)(0x80 | ((codepoint >> 6) & 0x3F));
        buf[2] = (char)(0x80 | (codepoint & 0x3F));
        return 3;
    }
    if (codepoint <= 0x10FFFF) {
        buf[0] = (char)(0xF0 | (codepoint >> 18));
        buf[1] = (char)(0x80 | ((codepoint >> 12) & 0x3F));
        buf[2] = (char)(0x80 | ((codepoint >> 6) & 0x3F));
        buf[3] = (char)(0x80 | (codepoint & 0x3F));
        return 4;
    }
    return 0;
}

int utf8_strlen(const char *s, int byte_len) {
    int count = 0;
    int i = 0;
    while (i < byte_len) {
        int cp;
        int n = utf8_decode(s + i, byte_len - i, &cp);
        if (n < 0) { i++; count++; }
        else { i += n; count++; }
    }
    return count;
}

int utf8_offset(const char *s, int byte_len, int n) {
    int i = 0;
    int count = 0;
    while (i < byte_len && count < n) {
        int cp;
        int nb = utf8_decode(s + i, byte_len - i, &cp);
        if (nb < 0) i++;
        else i += nb;
        count++;
    }
    return i;
}

int utf8_validate(const char *s, int byte_len) {
    int i = 0;
    while (i < byte_len) {
        int cp;
        int n = utf8_decode(s + i, byte_len - i, &cp);
        if (n < 0) return 0;
        i += n;
    }
    return 1;
}

int utf8_next(const char **p, const char *end) {
    if (*p >= end) return -1;
    int cp;
    int n = utf8_decode(*p, (int)(end - *p), &cp);
    if (n < 0) { (*p)++; return 0xFFFD; }
    *p += n;
    return cp;
}

/* ---- Case mapping tables ----
 * Sorted arrays of {from, to} pairs for binary search lookup.
 * Covers: Latin Extended, Greek, Cyrillic, Armenian, Georgian, Fullwidth
 */

typedef struct { int from; int to; } CaseMapping;

static int case_lookup(const CaseMapping *table, int n, int cp) {
    int lo = 0, hi = n - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        if (table[mid].from == cp) return table[mid].to;
        if (table[mid].from < cp) lo = mid + 1;
        else hi = mid - 1;
    }
    return cp;
}

