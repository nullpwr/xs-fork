/* Get Canonical_Combining_Class for a codepoint.
 * Returns 0 for base characters, non-zero for combining marks.
 * This is a simplified version covering the most common combining marks. */
static int get_ccc(int cp) {
    /* Combining Diacritical Marks (0300-036F) */
    if (cp >= 0x0300 && cp <= 0x0314) return 230;  /* above */
    if (cp >= 0x0315 && cp <= 0x0315) return 232;  /* above right */
    if (cp >= 0x0316 && cp <= 0x0319) return 220;  /* below */
    if (cp >= 0x031A && cp <= 0x031A) return 232;  /* above right */
    if (cp >= 0x031B && cp <= 0x031B) return 216;  /* attached above right */
    if (cp >= 0x031C && cp <= 0x0320) return 220;  /* below */
    if (cp >= 0x0321 && cp <= 0x0322) return 202;  /* attached below */
    if (cp >= 0x0323 && cp <= 0x0326) return 220;  /* below */
    if (cp >= 0x0327 && cp <= 0x0328) return 202;  /* attached below */
    if (cp >= 0x0329 && cp <= 0x0333) return 220;  /* below */
    if (cp >= 0x0334 && cp <= 0x0338) return 1;    /* overlay */
    if (cp >= 0x0339 && cp <= 0x033C) return 220;  /* below */
    if (cp >= 0x033D && cp <= 0x0344) return 230;  /* above */
    if (cp == 0x0345) return 240;                  /* iota subscript */
    if (cp >= 0x0346 && cp <= 0x034E) return 230;  /* above (approx) */
    if (cp >= 0x0350 && cp <= 0x0352) return 230;
    if (cp >= 0x0353 && cp <= 0x0356) return 220;
    if (cp >= 0x0357 && cp <= 0x0358) return 230;
    if (cp == 0x0359 || cp == 0x035A) return 220;
    if (cp >= 0x035B && cp <= 0x0362) return 230;
    if (cp >= 0x0363 && cp <= 0x036F) return 230;
    /* Hebrew */
    if (cp >= 0x0591 && cp <= 0x05BD) return 220;
    if (cp == 0x05BF) return 230;
    if (cp == 0x05C1) return 24;
    if (cp == 0x05C2) return 25;
    if (cp == 0x05C4) return 230;
    if (cp == 0x05C5) return 220;
    if (cp == 0x05C7) return 18;
    /* Arabic */
    if (cp >= 0x064B && cp <= 0x065F) return 230;
    if (cp == 0x0670) return 35;
    /* Devanagari nukta */
    if (cp == 0x093C) return 7;
    /* Generic: most combining marks above are 230, below 220 */
    if (cp >= 0x0483 && cp <= 0x0489) return 230;
    if (cp >= 0x1DC0 && cp <= 0x1DFF) return 230;
    if (cp >= 0x20D0 && cp <= 0x20F0) return 230;
    if (cp >= 0xFE20 && cp <= 0xFE2F) return 230;
    /* Zero for everything else */
    return 0;
}

/* Look up decomposition (binary search) */
static const Decomposition *find_decomp(int cp) {
    int lo = 0, hi = DECOMP_COUNT - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        if (decomp_table[mid].cp == cp) return &decomp_table[mid];
        if (decomp_table[mid].cp < cp) lo = mid + 1;
        else hi = mid - 1;
    }
    return NULL;
}

/* Look up composition */
static int find_comp(int base, int combining) {
    for (int i = 0; i < COMP_COUNT; i++) {
        if (comp_table[i].base == base && comp_table[i].combining == combining)
            return comp_table[i].composed;
    }
    return -1;
}

char *utf8_normalize_nfc(const char *s, int byte_len, int *out_len) {
    if (byte_len == 0) {
        char *r = xs_malloc(1);
        r[0] = '\0';
        if (out_len) *out_len = 0;
        return r;
    }

    /* Fast path: if all ASCII, already NFC */
    if (utf8_is_ascii(s, byte_len)) {
        char *r = xs_malloc(byte_len + 1);
        memcpy(r, s, byte_len);
        r[byte_len] = '\0';
        if (out_len) *out_len = byte_len;
        return r;
    }

    /* Step 1: decode to codepoints and decompose */
    int cap = byte_len * 2; /* overestimate */
    int *cps = xs_malloc(cap * sizeof(int));
    int len = 0;

    const char *p = s;
    const char *end = s + byte_len;
    while (p < end) {
        int cp;
        int n = utf8_decode(p, (int)(end - p), &cp);
        if (n < 0) { cp = 0xFFFD; n = 1; }
        p += n;

        /* Decompose if possible */
        const Decomposition *d = find_decomp(cp);
        if (d) {
            /* Recursively check if base itself decomposes */
            const Decomposition *d2 = find_decomp(d->base);
            if (d2) {
                if (len + 3 > cap) { cap *= 2; cps = xs_realloc(cps, cap * sizeof(int)); }
                cps[len++] = d2->base;
                cps[len++] = d2->combining;
            } else {
                if (len + 2 > cap) { cap *= 2; cps = xs_realloc(cps, cap * sizeof(int)); }
                cps[len++] = d->base;
            }
            cps[len++] = d->combining;
        } else {
            if (len + 1 > cap) { cap *= 2; cps = xs_realloc(cps, cap * sizeof(int)); }
            cps[len++] = cp;
        }
    }

    /* Step 2: sort combining marks by CCC (stable sort within runs) */
    for (int i = 1; i < len; i++) {
        int ccc_i = get_ccc(cps[i]);
        if (ccc_i == 0) continue; /* base character, don't move */
        /* bubble this combining mark leftward past others with higher CCC */
        int j = i;
        while (j > 0 && get_ccc(cps[j-1]) > ccc_i && get_ccc(cps[j-1]) != 0) {
            int tmp = cps[j]; cps[j] = cps[j-1]; cps[j-1] = tmp;
            j--;
        }
    }

    /* Step 3: canonical composition */
    int wi = 0;
    for (int i = 0; i < len; i++) {
        if (wi == 0) { cps[wi++] = cps[i]; continue; }

        int ccc = get_ccc(cps[i]);

        /* Find the starter (last character with CCC=0) */
        int starter = -1;
        for (int k = wi - 1; k >= 0; k--) {
            if (get_ccc(cps[k]) == 0) { starter = k; break; }
        }

        if (starter >= 0) {
            /* Check if any combining mark between starter and i blocks composition */
            int blocked = 0;
            for (int k = starter + 1; k < wi; k++) {
                if (get_ccc(cps[k]) >= ccc && ccc != 0) { blocked = 1; break; }
            }

            if (!blocked) {
                int composed = find_comp(cps[starter], cps[i]);
                if (composed >= 0) {
                    cps[starter] = composed;
                    continue; /* consumed the combining mark */
                }
            }
        }

        cps[wi++] = cps[i];
    }

    /* Step 4: encode back to UTF-8 */
    char *result = xs_malloc(wi * 4 + 1);
    int rlen = 0;
    for (int i = 0; i < wi; i++) {
        rlen += utf8_encode(cps[i], result + rlen);
    }
    result[rlen] = '\0';

    free(cps);
    if (out_len) *out_len = rlen;
    return result;
}
